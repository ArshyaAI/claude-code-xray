/**
 * planner.ts — Auto-generate PROGRAM.md from git history
 *
 * Reads recent git activity, TODOs in code, and open issues to produce
 * a prioritized task list for Shadow League experiments.
 *
 * CEO Review accepted expansion: "Auto-generate PROGRAM.md via /factory-planner"
 */

import { execSync } from "node:child_process";
import { writeFileSync, readFileSync, existsSync } from "node:fs";
import { join } from "node:path";

interface PlannedTask {
  description: string;
  context: string[];
  priority: "high" | "medium" | "low";
  source: "git-log" | "todo" | "issue" | "manual";
}

/**
 * Generate a PROGRAM.md from git history, TODOs, and repo analysis.
 *
 * @param repoRoot - Path to the repository
 * @param outputPath - Where to write PROGRAM.md (default: repoRoot/PROGRAM.md)
 * @param minTasks - Minimum tasks to generate (default: 8)
 */
export function generateProgramMd(
  repoRoot: string,
  outputPath?: string,
  minTasks = 8,
): { tasks: PlannedTask[]; path: string } {
  const tasks: PlannedTask[] = [];

  // Source 1: Recent git activity — files changed frequently need attention
  const recentChanges = getRecentlyChangedFiles(repoRoot);
  for (const file of recentChanges.slice(0, 5)) {
    tasks.push({
      description: `Review and improve ${file.path} (changed ${file.count} times in 30 days)`,
      context: [
        `  - Last commit: ${file.lastCommit}`,
        `  - High churn file — may need refactoring or better test coverage`,
      ],
      priority: file.count > 5 ? "high" : "medium",
      source: "git-log",
    });
  }

  // Source 2: TODO/FIXME comments in code
  const todos = findTodoComments(repoRoot);
  for (const todo of todos.slice(0, 5)) {
    tasks.push({
      description: `Fix ${todo.type} in ${todo.file}:${todo.line}: ${todo.text}`,
      context: [`  - Found in source code as ${todo.type} comment`],
      priority: todo.type === "FIXME" ? "high" : "medium",
      source: "todo",
    });
  }

  // Source 3: Files with no test coverage
  const untestedFiles = findUntestedFiles(repoRoot);
  for (const file of untestedFiles.slice(0, 3)) {
    tasks.push({
      description: `Add unit tests for ${file}`,
      context: [`  - No corresponding test file found`],
      priority: "medium",
      source: "manual",
    });
  }

  // Pad with generic improvement tasks if needed
  while (tasks.length < minTasks) {
    const padding = [
      "Improve error messages for better developer experience",
      "Add input validation to public API functions",
      "Reduce code duplication in utility functions",
      "Add JSDoc comments to exported functions",
      "Improve type safety with stricter TypeScript checks",
      "Add logging for debugging production issues",
      "Optimize hot paths for better performance",
      "Update dependencies to latest versions",
    ];
    const idx = tasks.length - Math.max(0, tasks.length - minTasks);
    if (idx < padding.length && padding[idx]) {
      tasks.push({
        description: padding[idx],
        context: [],
        priority: "low",
        source: "manual",
      });
    } else {
      break;
    }
  }

  // Format as PROGRAM.md
  const lines: string[] = ["# Factory Work Items\n"];
  lines.push(
    `# Auto-generated by factory plan on ${new Date().toISOString().slice(0, 10)}\n`,
  );

  for (const task of tasks) {
    lines.push(`- [ ] ${task.description}`);
    for (const ctx of task.context) {
      lines.push(ctx);
    }
  }
  lines.push("");

  const outPath = outputPath ?? join(repoRoot, "PROGRAM.md");
  writeFileSync(outPath, lines.join("\n"), "utf-8");

  return { tasks, path: outPath };
}

// ─── Helpers ─────────────────────────────────────────────────────────────────

interface FileChange {
  path: string;
  count: number;
  lastCommit: string;
}

function getRecentlyChangedFiles(repoRoot: string): FileChange[] {
  try {
    const raw = execSync(
      'git log --since=30.days --name-only --format="" | sort | uniq -c | sort -rn | head -10',
      { cwd: repoRoot, encoding: "utf-8", stdio: ["pipe", "pipe", "pipe"] },
    );
    return raw
      .trim()
      .split("\n")
      .filter((l) => l.trim())
      .map((line) => {
        const match = line.trim().match(/^(\d+)\s+(.+)$/);
        if (!match?.[1] || !match[2]) return null;
        const lastCommit = getLastCommitForFile(repoRoot, match[2]);
        return { path: match[2], count: parseInt(match[1], 10), lastCommit };
      })
      .filter((f): f is FileChange => f !== null);
  } catch {
    return [];
  }
}

function getLastCommitForFile(repoRoot: string, file: string): string {
  try {
    return execSync(`git log --format="%s" -1 -- "${file}"`, {
      cwd: repoRoot,
      encoding: "utf-8",
      stdio: ["pipe", "pipe", "pipe"],
    }).trim();
  } catch {
    return "unknown";
  }
}

interface TodoComment {
  file: string;
  line: number;
  type: "TODO" | "FIXME" | "HACK";
  text: string;
}

function findTodoComments(repoRoot: string): TodoComment[] {
  try {
    const raw = execSync(
      'grep -rn "TODO\\|FIXME\\|HACK" src/ --include="*.ts" 2>/dev/null | head -20',
      { cwd: repoRoot, encoding: "utf-8", stdio: ["pipe", "pipe", "pipe"] },
    );
    return raw
      .trim()
      .split("\n")
      .filter((l) => l.trim())
      .map((line) => {
        const match = line.match(/^(.+?):(\d+):.*?(TODO|FIXME|HACK):?\s*(.*)$/);
        if (!match) return null;
        return {
          file: match[1] ?? "",
          line: parseInt(match[2] ?? "0", 10),
          type: (match[3] as "TODO" | "FIXME" | "HACK") ?? "TODO",
          text: match[4]?.trim() ?? "",
        };
      })
      .filter((t): t is TodoComment => t !== null);
  } catch {
    return [];
  }
}

function findUntestedFiles(repoRoot: string): string[] {
  try {
    const srcFiles = execSync(
      'find src -name "*.ts" ! -name "*.test.ts" ! -name "*.d.ts" -type f 2>/dev/null',
      { cwd: repoRoot, encoding: "utf-8", stdio: ["pipe", "pipe", "pipe"] },
    )
      .trim()
      .split("\n")
      .filter((f) => f.trim());

    return srcFiles.filter((f) => {
      const testFile = f.replace(".ts", ".test.ts");
      const testDir = f.replace(/\/([^/]+)\.ts$/, "/__tests__/$1.test.ts");
      return (
        !existsSync(join(repoRoot, testFile)) &&
        !existsSync(join(repoRoot, testDir))
      );
    });
  } catch {
    return [];
  }
}
