// ─── Render ───────────────────────────────────────────────────────────────

/**
 * Render a guardian sprite for the given bones and animation frame.
 * Returns an array of 5 strings (lines).
 */
export function renderGuardian(bones: GuardianBones, frame: number): string[] {
  const stageSprites = SPRITES[bones.species][bones.stage];
  const sprite = stageSprites[frame % stageSprites.length]!;
  return sprite.map((line) => line.replaceAll("{E}", bones.eye));
}

/**
 * Number of animation frames available for any stage.
 */
export function guardianFrameCount(): number {
  return 3;
}
